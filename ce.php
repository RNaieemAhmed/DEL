<center><h1>CODE OF ETHICS</h1></center>
<ul>
<h>1.Integrity</h><br><br>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Shall perform their work with honesty, diligence, and responsibility.</p>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>The integrity of internal auditors establishes trust and thus provides the basis for reliance on their judgment.</p>

<h>2.Objectivity</h><br><br>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Internal auditors exhibit the highest level of professional objectivity in gathering, evaluating, and communicating information about the activity or process being examined. Internal auditors make a balanced assessment of all the relevant circumstances and are not unduly influenced by their own interests or by others in forming judgments.</p>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Shall not accept anything that may impair or be presumed to impair their professional judgment.</p>


<h>3.Confidentiality</h><br><br>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Shall be prudent in the use and protection of information acquired in the course of their duties.</p>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Internal auditors respect the value and ownership of information they receive and do not disclose information without appropriate authority unless there is a legal or professional obligation to do so.</p>

<h>4.Competency</h><br><br>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Internal auditors apply the knowledge, skills, and experience needed in the performance of internal audit services.</p>
<p><i class="fa fa-angle-double-right " style="color:red;font-size: larger;"></i>Shall engage only in those services for which they have the necessary knowledge, skills, and experience.
</p>
</ul>
